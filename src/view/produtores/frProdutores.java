    private void btnDeletar6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeletar6MouseClicked
        if (tblProdutores.getSelectedRow() != -1) {
            ProdutorController produtor = new ProdutorController();
            int linhaSelecionada = tblProdutores.getSelectedRow();
            String textoCelula = tblProdutores.getValueAt(linhaSelecionada, 0).toString();
            int idProdutora = Integer.parseInt(textoCelula);
            if (JOptionPane.showConfirmDialog(null, "tem certeza que deseja deletar essa editora?") == 0) {
                produtor.removerProdutor(idProdutora);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma editora selecionada");
        }
        pesquisar();
    }//GEN-LAST:event_btnDeletar6MouseClicked

