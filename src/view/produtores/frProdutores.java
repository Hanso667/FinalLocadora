    private void btnAlterar6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlterar6MouseClicked
        if (tblProdutores.getSelectedRow() != -1) {
            int linhaSelecionada = tblProdutores.getSelectedRow();
            String textoCelula = tblProdutores.getValueAt(linhaSelecionada, 0).toString();
            int idProdutor = Integer.parseInt(textoCelula);
            new frAlterarProdutor(null, rootPaneCheckingEnabled, idProdutor).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum usuario selecionado");
        }
        pesquisar();
    }//GEN-LAST:event_btnAlterar6MouseClicked

